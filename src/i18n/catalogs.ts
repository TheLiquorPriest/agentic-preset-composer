export const APC_CATALOGS = Object.freeze({
  en: EN_CATALOG,
  zh: ZH_CATALOG,
  "zh-TW": ZH_TW_CATALOG,
  ja: JA_CATALOG,
  fr: FR_CATALOG,
  it: IT_CATALOG,
} satisfies Readonly<Record<ApcLocale, ApcCatalog>>)

const HTML_ESCAPE: Readonly<Record<string, string>> = Object.freeze({
  "&": "&amp;",
  "<": "&lt;",
  ">": "&gt;",
  '"': "&quot;",
  "'": "&#39;",
})

const PLACEHOLDER_PATTERN = /\{\{([A-Za-z][A-Za-z0-9_.-]*)\}\}/g

function textValue(value: unknown): string {
  if (value === null || value === undefined) {
    return ""
  }

  switch (typeof value) {
    case "string":
      return value
    case "number":
    case "boolean":
    case "bigint":
    case "symbol":
      return String(value)
    default:
      try {
        return String(value)
      } catch {
        return ""
      }
  }
}

export function interpolate(
  template: string,
  values: Readonly<Record<string, unknown>> = {},
): string {
  if (typeof template !== "string") {
    return ""
  }

  const source = values !== null && typeof values === "object" ? values : undefined
  return template.replace(PLACEHOLDER_PATTERN, (token, name: string) => {
    try {
      if (source === undefined || !Object.prototype.hasOwnProperty.call(source, name)) {
        return token
      }
      return textValue(source[name]).replace(
        /[&<>"']/g,
        (character) => HTML_ESCAPE[character] ?? character,
      )
    } catch {
      return token
    }
  })
}

export function normalizeLocale(locale: unknown): ApcLocale {
  if (typeof locale !== "string") {
    return "en"
  }

  const normalized = locale.trim().replaceAll("_", "-").toLowerCase()
  if (
    normalized === "zh-tw" ||
    normalized.startsWith("zh-tw-") ||
    normalized === "zh-hant" ||
    normalized.startsWith("zh-hant-")
  ) {
    return "zh-TW"
  }
  if (normalized === "zh" || normalized.startsWith("zh-")) {
    return "zh"
  }

  const primary = normalized.split("-", 1)[0]
  if (primary === "en" || primary === "ja" || primary === "fr" || primary === "it") {
    return primary
  }
  return "en"
}

export function getCatalog(locale: unknown): ApcCatalog {
  return APC_CATALOGS[normalizeLocale(locale)]
}

function replaceText(
  template: string,
  values: Readonly<Record<string, unknown>> = {},
): string {
  const source = values !== null && typeof values === "object" ? values : undefined
  return template.replace(PLACEHOLDER_PATTERN, (token, name: string) => {
    try {
      if (source === undefined || !Object.prototype.hasOwnProperty.call(source, name)) {
        return token
      }
      return textValue(source[name])
    } catch {
      return token
    }
  })
}

export function formatCatalogText(
  locale: unknown,
  key: ApcCatalogKey,
  values: Readonly<Record<string, unknown>> = {},
): string {
  let template: unknown
  try {
    template = getCatalog(locale)[key]
  } catch {
    return ""
  }
  return typeof template === "string" ? replaceText(template, values) : ""
}

export function createApcTranslator(getLocale: () => unknown): ApcTranslate {
  return (key, values) => {
    let locale: unknown
    try {
      locale = getLocale()
    } catch {
      locale = "en"
    }
    return formatCatalogText(locale, key, values)
  }
}
